// 3: location Object 
// window.location.href (property) 
// Returns or sets the complete URL of the current page. 
// Ex:- console.log(window.location.href) 

// window.location.hostname (property)  
// Returns the double name of the web host. 
// Ex:- console.log(window.location.hostname) 

// window.location.assign(url) (method): 
// Navigate to the specified URL. 
// Ex:-window.location.assign(https://www.example.com) 

// window.location.reload(forceReload) (Method) 
