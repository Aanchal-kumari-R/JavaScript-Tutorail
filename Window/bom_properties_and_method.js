// ** Window Properties :- 

//  Window.Console 
// Represents the browser's debugging console.  
// Allow login messages using method like log(), warn() , and erroe(). 

// window.InnerWidth / window.Innerheight 
// Provides the window width and height of the browser's content area(excluding toolbars and scrollbars).    

// Window.scrollX / window.scrollY 
// Represents the number of pixels that the document is currently scrolled horizontally and vertically. 

// window.OuterWidth / window.OuterHeight: 
// provide the width and height of the whole browser window (including toolbars and borders). 

// window.localStorage 
// Provides a way to store key-value pairs persistently on the user's device.
// Allow storing data beyond the lifetime of a single page session. 

// Function alert/confirm/prompt are also a part of BOM. 

// **  Window Object:-  
// Represent the global window containig the BOM. 
// Ex:- Window.location, window.InnerWidth, window.Innerheight  

// Navigator Object 
// Provides information about the browser.  
// Ex:- Navigator.userAgent , Navigator.language 

// location Object 
// Represent the current url of the browser. 
// Ex:-  Window.location.href,  Window.location.hostname 

// history Object 
// Represent the session history , allowing navigation through the browser history.  
// Ex:- Window.history.back(),  Window.history.forward() 

// Screen Object 
// Represents information about the user's screen. 
//  Ex:-  Window.screen.height, Window.screen.width 

// Window.localStorage , window.sessionStorage 
// Objects for storing data persistently of for the duration of a page session. 
 
// document Object 
// Represent the Dom of the currently displayed document. 

// navigator Object 
// The JavaScript navigator object is used for browser detection. It can be used to get browser information such as appName,  
// appCodeName , UserAgent etc. 

// navigator.userAgent (property) 
// Returns the user agent string of the browser. 
// Ex:- console.log(navigator.userAgent) 

// navigator.language (property) 
// Returns the language prefernce of the user's browser. 
// Ex:- console.log(navigator.language) 

// navigator.cookieEnabled (property)  
// Indicates wheather cookies are enabled in the browser. 
// Ex:- console.log(navigator.cookieEnabled) 

// navigator.platform (property) 
// Returns the platform on which the browser is running. 
// Ex:- console.log(navigator.platform)  

// navigator.online (property) 
// Indicate whether the browser is online. 
// Ex:- console.log(navigator.online) 

